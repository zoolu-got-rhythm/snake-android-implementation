package com.example.slurp.myapplication;

public class Board {
    int xTiles, yTiles;

    public Board(int xTiles, int yTiles){
        this.xTiles = xTiles;
        this.yTiles = yTiles;
    }

    public int getxTiles() {
        return xTiles;
    }

    public int getyTiles() {
        return yTiles;
    }
}
